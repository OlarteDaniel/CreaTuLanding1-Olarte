export const getProductsByCategory = (category) => {
    return new Promise ((resolve) =>{
        const productosFiltrados = productos.filter((el) => el.categoria === category)
        setTimeout(() =>{
            resolve(productosFiltrados);
        },2000)
    })
}

export const getProductsById = (id) => {
    return new Promise ((resolve) => {
        const productoFiltado = productos.find((el) => el.id === parseInt(id));
        setTimeout(() =>{
            resolve(productoFiltado);
        },2000)
    })
}